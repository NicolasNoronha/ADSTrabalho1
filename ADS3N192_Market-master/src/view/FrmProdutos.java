    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        String nome = txtNome.getText();
        String quantidade = txtQuantidade.getText();
        if (nome.isEmpty()){
            JOptionPane.showConfirmDialog(this, "Voce deve preencher o nome do produto");
        }
        if (quantidade.isEmpty()){
            JOptionPane.showConfirmDialog(this, "Voce deve preencher o quantidade do produto");
        }
        
        else{
            Produtos pro = new Produtos();
            pro.setNome(nome);
            pro.setQuantidade(quantidade);
            ProdutosDAO.inserir(pro);
            txtNome.setText("");
            txtQuantidade.setText("");
            
        
        }
        
    }//GEN-LAST:event_btnSalvarActionPerformed
